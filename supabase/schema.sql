create table if not exists public.catalogo_campos_prestacion (
  id integer generated by default as identity primary key,
  prestacion_id integer not null references public.catalogo_prestaciones(id) on delete cascade,
  campo_key text not null,
  label text not null,
  tipo text not null,
  placeholder text,
  ayuda text,
  requerido boolean not null default false,
