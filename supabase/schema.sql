create table if not exists public.estado_conversaciones (
  id integer generated by default as identity primary key,
  conversacion_id integer not null references public.conversaciones_whatsapp(id) on delete cascade,
  usuario_id integer references public.usuarios(id) on delete set null,
  isapre_id public.isapre_id,
  etapa public.etapa_conversacion not null default 'idle',
  prestacion_id integer references public.catalogo_prestaciones(id) on delete set null,
  campo_actual_id integer references public.catalogo_campos_prestacion(id) on delete set null,
  proceso_demo_id integer references public.procesos_demo(id) on delete set null,
  payload jsonb not null default '{}'::jsonb,
  metadata jsonb not null default '{}'::jsonb,
  last_message_id text,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  unique (conversacion_id)
);

