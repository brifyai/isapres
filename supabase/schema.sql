create extension if not exists pgcrypto;

do $$
begin
  if not exists (select 1 from pg_type where typname = 'isapre_id') then
    create type public.isapre_id as enum (
      'colmena',
      'banmedica',
      'consalud',
      'cruzblanca',
      'nueva_masvida',
      'vida_tres',
      'esencial'
    );
  end if;

  if not exists (select 1 from pg_type where typname = 'estado_solicitud') then
    create type public.estado_solicitud as enum (
      'en_cola',
      'procesando_ocr',
      'iniciando_sesion',
      'subiendo_boleta',
      'exitoso',
      'rechazado'
    );
  end if;

  if not exists (select 1 from pg_type where typname = 'portal_status') then
    create type public.portal_status as enum (
      'operativo',
      'caido',
      'html_cambiado',
      'mantenimiento'
    );
  end if;

  if not exists (select 1 from pg_type where typname = 'estado_proceso_demo') then
    create type public.estado_proceso_demo as enum (
      'pendiente',
      'en_progreso',
      'completado',
      'fallido'
    );
  end if;

  if not exists (select 1 from pg_type where typname = 'direccion_mensaje') then
    create type public.direccion_mensaje as enum (
      'entrante',
      'saliente',
      'sistema'
    );
  end if;

  if not exists (select 1 from pg_type where typname = 'tipo_mensaje_whatsapp') then
    create type public.tipo_mensaje_whatsapp as enum (
      'text',
      'image',
      'interactive',
      'document',
      'audio',
      'system'
    );
  end if;

  if not exists (select 1 from pg_type where typname = 'etapa_conversacion') then
    create type public.etapa_conversacion as enum (
      'idle',
      'awaiting_prestacion',
      'awaiting_field',
      'processing',
      'completed'
    );
  end if;
end $$;

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

create table if not exists public.usuarios (
  id integer generated by default as identity primary key,
  nombre text not null,
  telefono text not null unique,
  rut text not null,
  password_hash text not null,
  accepted_privacy_policy_at timestamptz,
  accepted_terms_at timestamptz,
  consent_ip text,
  consent_user_agent text,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

alter table public.usuarios
  add column if not exists accepted_privacy_policy_at timestamptz,
  add column if not exists accepted_terms_at timestamptz,
  add column if not exists consent_ip text,
  add column if not exists consent_user_agent text;

create table if not exists public.credenciales_isapre (
  id integer generated by default as identity primary key,
  usuario_id integer not null references public.usuarios(id) on delete cascade,
  isapre_id public.isapre_id not null,
  rut text not null,
  password_encrypted text not null,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  unique (usuario_id, isapre_id)
);

create table if not exists public.reembolsos (
  id integer generated by default as identity primary key,
  usuario_id integer not null references public.usuarios(id) on delete cascade,
  rut_usuario text not null,
  isapre public.isapre_id not null,
  monto integer not null default 0 check (monto >= 0),
  fecha timestamptz not null default timezone('utc', now()),
  estado public.estado_solicitud not null default 'en_cola',
  url_documento text not null,
  folio_isapre text,
  error text,
  intentos integer not null default 0 check (intentos >= 0),
  locked_at timestamptz,
  worker_id text,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.portales_status (
  isapre_id public.isapre_id primary key,
  status public.portal_status not null default 'operativo',
  ultima_ejecucion_exitosa timestamptz not null default timezone('utc', now()),
  latencia_ms integer,
  mensaje_error text,
  updated_at timestamptz not null default timezone('utc', now()),
  constraint portales_status_latencia_ms_check
    check (latencia_ms is null or latencia_ms >= 0)
);

create table if not exists public.conversaciones_whatsapp (
  id integer generated by default as identity primary key,
  usuario_id integer references public.usuarios(id) on delete set null,
  telefono text not null,
  canal text not null default 'whatsapp',
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  unique (telefono, canal)
);

create table if not exists public.mensajes_whatsapp (
  id integer generated by default as identity primary key,
  conversacion_id integer not null references public.conversaciones_whatsapp(id) on delete cascade,
  direccion public.direccion_mensaje not null,
  tipo public.tipo_mensaje_whatsapp not null default 'text',
  contenido text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.webhook_events (
  id integer generated by default as identity primary key,
  idempotency_key text not null unique,
  event_name text not null,
  payload jsonb not null default '{}'::jsonb,
